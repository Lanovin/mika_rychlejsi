export function t(key: string, lang: Lang): string {
  return translations[key]?.[lang] ?? key;
}

export function tReplace(key: string, lang: Lang, replacements: Record<string, string | number>): string {
  let result = t(key, lang);
  for (const [placeholder, value] of Object.entries(replacements)) {
    result = result.replace(`{${placeholder}}`, String(value));
  }
  return result;
}

/** Return the translated string if key exists, otherwise the original Czech value. */
export function tc(key: string, lang: Lang, fallback: string): string {
  if (lang === "cs") return fallback;
  const entry = translations[key];
  return entry ? entry.en : fallback;
}
